class Department{
  int? college_link;
  int? id;
  String? name;
  String? helpno;
  String? userid;
  String? password;

  Department(college_link,id,name,helpno,userid,password){
    this.id=id;
    this.name=name;
    this.college_link=college_link;
    this.userid=userid;
    this.password=password;
    this.helpno=helpno;
  }
}
